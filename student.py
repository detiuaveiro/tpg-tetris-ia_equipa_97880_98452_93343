# checks if the piece is an I, b[0] says if it's horizontal, b[1] says if it's vertical
def discover_i(piece):
    if piece != None:
        x = piece[0][0]
        y = piece[0][1]
        b = [True,True]
        for coor in piece:
            if coor[0] != x:
                b[0] = False
            if coor[1] != y:
                b[1] = False
        return b  
    return [False,False]

def findState(state, game):
    for block in game:
        state[ block[0]-1 ] = block[1]
    return state

